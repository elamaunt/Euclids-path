# ------------------------------------------------------- arithmetic re-checks

def is_prime(n):
    if n < 2:
        return False
    d = 2
    while d * d <= n:
        if n % d == 0:
            return False
        d += 1
    return True


def verify_clock_tables(geh):
    assert I6 == geh.LEAN_I6, "i6 table drifted from the harness LEAN_I6"
    for q, i6 in I6.items():
        assert is_prime(q), f"clock {q} not prime"
        assert 0 < i6 < q, f"i6 out of range at {q}"
        assert (6 * i6) % q == 1, f"6*i6 != 1 mod {q}"
        assert (2 * i6) % q != 0, f"degenerate two-phase branch at {q}"
    got = [q for q, _ in BIGS41]
    assert got == sorted((q for q in I6 if 13 < q <= A41), reverse=True), \
        "BIGS41 must be the big clocks of scale 41, descending"
    for q, i6 in SMALLS + BIGS41:
        assert I6[q] == i6, f"clock pair ({q}, {i6}) contradicts the i6 table"


def verify_sat_window(r, A, G):
    """Trial-division re-verification of a SAT witness: r clean, r+G clean, and
    EVERY center r+1 .. r+G-1 struck by some clock q <= A (mod check per clock
    per offset -- the exact arithmetic content of allStruckB)."""
    clocks = [q for q in sorted(I6) if q <= A]

    def struck(m):
        return any((6 * m - 1) % q == 0 or (6 * m + 1) % q == 0 for q in clocks)

    assert not struck(r), f"A={A}: r={r} is not clean"
    assert not struck(r + G), f"A={A}: r+G={r + G} is not clean"
    for off in range(1, G):
        assert struck(r + off), f"A={A}: offset {off} of r={r} not struck"
    return clocks


# ------------------------------------------------------------- cell re-solves

def load_and_verify_cells(geh):
    with open(os.path.join(REPO, "tools", "sharp41_cells.json"), encoding="ascii") as f:
        data = json.load(f)
    assert (data["A"], data["g"], data["G"]) == (A41, GSHARP41, G41)
    cells = data["cells"]
    assert data["n_cells"] == len(cells) == 5 * 7 * 11 == 385
    assert {(c["t5"], c["t7"], c["t11"]) for c in cells} == \
        {(a, b, c) for a in range(5) for b in range(7) for c in range(11)}, \
        "cells must exhaust the full t5 x t7 x t11 grid"
    tot_cap = sum(c["cap_work"] for c in cells)
    tot_nodes = sum(c["nodes"] for c in cells)
    assert tot_cap == data["total_cap_work"]
    assert tot_nodes == data["total_nodes"]
    assert max(c["cap_work"] for c in cells) == data["max_cell_cap_work"]
    # re-solve EVERY cell against the pinned lean-model replica
    for c in cells:
        unsat, st = geh.lean_model_decision(
            GSHARP41, A41, fix={5: c["t5"], 7: c["t7"], 11: c["t11"]})
        assert unsat, f"cell {c} must be UNSAT"
        assert st["cap_work"] == c["cap_work"] and st["nodes"] == c["nodes"], \
            f"cell {c} re-solve mismatch: {st['cap_work']}, {st['nodes']}"
    # undecomposed cross-check
    unsat, st = geh.lean_model_decision(GSHARP41, A41)
    assert unsat and st["cap_work"] == tot_cap and st["nodes"] == tot_nodes, \
        "undecomposed run disagrees with the cell sums"
    return data, cells


def split_big_cells(geh, cells):
    """Cells above CEIL are split by t13; each sub-cell is re-solved, the 13
    sub-cell cap-works must sum EXACTLY to the parent and each must be < CEIL
    (a residual > CEIL would require restructuring around t17 -- abort loudly)."""
    units = []
    n_split = 0
    for c in cells:
        if c["cap_work"] <= CEIL:
            units.append(dict(c, subs=None))
            continue
        n_split += 1
        subs = []
        for t13 in range(13):
            unsat, st = geh.lean_model_decision(
                GSHARP41, A41,
                fix={5: c["t5"], 7: c["t7"], 11: c["t11"], 13: t13})
            assert unsat, f"sub-cell {c} t13={t13} must be UNSAT"
            assert st["cap_work"] < CEIL, (
                f"sub-cell {c} t13={t13} still exceeds CEIL "
                f"({st['cap_work']}) -- would need a t17 restructuring; ABORT")
            subs.append(dict(t13=t13, cap_work=st["cap_work"], nodes=st["nodes"]))
        assert sum(s["cap_work"] for s in subs) == c["cap_work"], \
            f"sub-cell cap-work does not sum to parent at {c}"
        assert sum(s["nodes"] for s in subs) == c["nodes"], \
            f"sub-cell nodes do not sum to parent at {c}"
        units.append(dict(c, subs=subs))
    return units, n_split


def pack_parts(units):
    """Greedy sequential packing (units stay in JSON order: descending cap-work,
    deterministic).  A split cell travels as ONE unit (its 13 sub-cells + glue
    stay in the same part)."""
    parts, cur, cur_w = [], [], 0
    for u in units:
        if cur and cur_w + u["cap_work"] > PART_CAP:
            parts.append(cur)
            cur, cur_w = [], 0
        cur.append(u)
        cur_w += u["cap_work"]
    if cur:
        parts.append(cur)
    for p in parts:
        assert sum(u["cap_work"] for u in p) <= PART_CAP
    assert sum(len(p) for p in parts) == len(units)
    return parts


# --------------------------------------------------------------- Lean emission

def cell_name(u):
    return f"unsat41s_c{u['t5']}_{u['t7']}_{u['t11']}"


def strike_prefix(u, with_t13=None):
    """The nested strikeFilter term for a pinned cell (innermost clock first: 5, 7, 11[, 13])."""
    s = (f"(strikeFilter 11 2 {u['t11']} (strikeFilter 7 6 {u['t7']} "
         f"(strikeFilter 5 1 {u['t5']} (List.range' 1 {GSHARP41}))))")
    if with_t13 is not None:
        s = f"(strikeFilter 13 11 {with_t13} {s})"
    return s


def emit_whole_cell(u):
    return f"""set_option maxRecDepth 100000 in
set_option maxHeartbeats 0 in
/-- Cell `(t5, t7, t11) = ({u['t5']}, {u['t7']}, {u['t11']})` — cap-work {u['cap_work']:,}, nodes {u['nodes']:,} (measured, re-verified). -/
theorem {cell_name(u)} :
    enumSmallB [(13, 11)] bigs41
      {strike_prefix(u)} = true := by
  decide +kernel
"""


def emit_split_cell(u):
    out = []
    for s in u["subs"]:
        out.append(f"""set_option maxRecDepth 100000 in
set_option maxHeartbeats 0 in
-- sub-cell t13 = {s['t13']}: cap-work {s['cap_work']:,}, nodes {s['nodes']:,}
theorem {cell_name(u)}_s{s['t13']} :
    enumSmallB [] bigs41
      {strike_prefix(u, with_t13=s['t13'])} = true := by
  decide +kernel
""")
    bullets = "\n".join(f"  · exact {cell_name(u)}_s{s['t13']}" for s in u["subs"])
    out.append(f"""/-- Cell `(t5, t7, t11) = ({u['t5']}, {u['t7']}, {u['t11']})` — cap-work {u['cap_work']:,} > {CEIL:,}:
    split by the `t13` phase into 13 kernel sub-cells (sums re-verified exact). -/
theorem {cell_name(u)} :
    enumSmallB ((13, 11) :: []) bigs41
      {strike_prefix(u)} = true := by
  refine enumSmall_cons_of_parts ?_
  intro t ht
  interval_cases t
{bullets}
""")
    return "\n".join(out)


