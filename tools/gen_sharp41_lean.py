def emit_build_script(n_parts):
    steps = ["Step00GapBracket4143"] + [f"Part{i + 1:02d}" for i in range(n_parts)] \
        + ["Step00Sharp41"]
    lines = []
    for s in steps:
        if s.startswith("Part"):
            src = f"EuclidsPath\\Engine\\Sharp41\\{s}.lean"
            olean = f".lake\\build\\lib\\lean\\EuclidsPath\\Engine\\Sharp41\\{s}.olean"
        else:
            src = f"EuclidsPath\\Engine\\{s}.lean"
            olean = f".lake\\build\\lib\\lean\\EuclidsPath\\Engine\\{s}.olean"
        lines.append(f"@{{ Name = '{s}'; Src = '{src}'; Olean = '{olean}' }}")
    steps_ps = ",\n  ".join(lines)
    return f"""# GENERATED by tools/gen_sharp41_lean.py -- sequential, RESUMABLE kernel build of the
# Sharp-41 ladder: bracket module, {n_parts} part files, assembly.  One part at a time
# (LEAN_NUM_THREADS = 1), each `lake env lean -o <olean> <src>`; progress appended to
# tools/sharp41_build.log -- re-running the script skips parts already marked OK.
param([switch]$Force)

$repo = Split-Path $PSScriptRoot -Parent
Set-Location $repo
$env:LEAN_NUM_THREADS = '1'
$log = Join-Path $repo 'tools\\sharp41_build.log'

$steps = @(
  {steps_ps}
)

foreach ($s in $steps) {{
  $name = $s.Name
  if (-not $Force -and (Test-Path $log) -and
      (Select-String -Path $log -Pattern ('^OK ' + $name + ' ') -Quiet)) {{
    Write-Output ('skip ' + $name + ' (already OK in log)')
    continue
  }}
  $dir = Split-Path $s.Olean
  if (-not (Test-Path $dir)) {{ New-Item -ItemType Directory -Force $dir | Out-Null }}
  $sw = [Diagnostics.Stopwatch]::StartNew()
  lake env lean -o $s.Olean $s.Src
  $code = $LASTEXITCODE
  $sw.Stop()
  $secs = [math]::Round($sw.Elapsed.TotalSeconds, 1)
  $tag = if ($code -eq 0) {{ 'OK' }} else {{ 'FAIL' }}
  $line = ('{{0}} {{1}} exit={{2}} wall={{3}}s at {{4}}' -f $tag, $name, $code, $secs,
           (Get-Date -Format s))
  Add-Content -Path $log -Value $line
  Write-Output $line
  if ($code -ne 0) {{ exit 1 }}
}}
Add-Content -Path $log -Value ('DONE ladder at ' + (Get-Date -Format s))
Write-Output 'DONE ladder'
"""


# ----------------------------------------------------------------------- main

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--dry-run", action="store_true",
                    help="verify + print the plan, write nothing")
    args = ap.parse_args()

    geh = load_harness()
    verify_clock_tables(geh)

    print("re-verifying SAT witnesses arithmetically (mod check per clock per offset) ...")
    c41 = verify_sat_window(R41, A41, G41)
    print(f"  A=41: r={R41}: r clean, r+{G41} clean, centers r+1..r+{G41 - 1} all struck "
          f"(clocks {c41})")
    c43 = verify_sat_window(R43, A43, G43)
    print(f"  A=43: r={R43}: r clean, r+{G43} clean, centers r+1..r+{G43 - 1} all struck "
          f"(clocks {c43})")
    assert R41 == geh.SAT_WITNESS_R[41] and R43 == geh.SAT_WITNESS_R[43]
    assert G41 == geh.EXPECTED_G[41] and G43 == geh.EXPECTED_G[43]

    print("re-solving all 385 cells against tools/sharp41_cells.json ...")
    data, cells = load_and_verify_cells(geh)
    print(f"  all 385 cells match (total cap-work {data['total_cap_work']:,}, "
          f"nodes {data['total_nodes']:,})")

    print(f"splitting cells above CEIL = {CEIL:,} by t13 ...")
    units, n_split = split_big_cells(geh, cells)
    n_gates = sum(1 if u["subs"] is None else len(u["subs"]) for u in units)
    max_gate = max(u["cap_work"] if u["subs"] is None else
                   max(s["cap_work"] for s in u["subs"]) for u in units)
    print(f"  {n_split} cells split into 13 t13-sub-cells each (sums exact); "
          f"{len(units) - n_split} whole cells; {n_gates} kernel gates; "
          f"max gate cap-work {max_gate:,} < {CEIL:,}")

    parts = pack_parts(units)
    total_cap = sum(u["cap_work"] for u in units)
    print(f"packing: {len(parts)} parts, PART_CAP = {PART_CAP:,} cap-work "
          f"(<= ~450 s at the 0.06 ms/unit model; measured ~4.4 us/unit => ~33 s kernel/part)")
    for i, p in enumerate(parts):
        w = sum(u["cap_work"] for u in p)
        print(f"  Part{i + 1:02d}: {len(p):3d} cells, cap-work {w:>9,}, "
              f"predicted kernel {w * 0.06e-3:6.1f} s (model) / {w * 4.4e-6:5.1f} s (measured)")

    if args.dry_run:
        print("dry run: nothing written")
        return

    os.makedirs(PARTS_DIR, exist_ok=True)
    wrote = []

    def write(path, text):
        with open(path, "w", encoding="utf-8", newline="\n") as f:
            f.write(text)
        wrote.append(os.path.relpath(path, REPO))

    write(os.path.join(ENGINE, "Step00GapBracket4143.lean"), emit_bracket())
    for i, p in enumerate(parts):
        write(os.path.join(PARTS_DIR, f"Part{i + 1:02d}.lean"),
              emit_part(i + 1, len(parts), p, total_cap))
    write(os.path.join(ENGINE, "Step00Sharp41.lean"), emit_assembly(units, parts))
    write(os.path.join(REPO, "tools", "sharp41_build.ps1"),
          emit_build_script(len(parts)))

    # post-write audit: re-read the emitted parts and re-extract the cell grid
    import re
    seen = set()
    for i in range(len(parts)):
        with open(os.path.join(PARTS_DIR, f"Part{i + 1:02d}.lean"), encoding="utf-8") as f:
            text = f.read()
        for m in re.finditer(r"theorem unsat41s_c(\d+)_(\d+)_(\d+) :", text):
            seen.add((int(m.group(1)), int(m.group(2)), int(m.group(3))))
    assert seen == {(a, b, c) for a in range(5) for b in range(7) for c in range(11)}, \
        "post-write audit: emitted cell theorems do not exhaust the phase grid"
    print("post-write audit: the emitted cell theorems exhaust the full 5 x 7 x 11 grid")
    print("wrote:")
    for w in wrote:
        print("  " + w)


if __name__ == "__main__":
    main()
